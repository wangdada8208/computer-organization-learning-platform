def single(
    qid: str,
    source: str,
    section_id: str,
    section_title: str,
    stem: str,
    options: list[str],
    answer_index: int,
    explanation: str,
    related: str,
    tag: str,
    option_notes: list[str] | None = None,
) -> dict:
    notes = option_notes or [
        "该选项不符合题意。" if i != answer_index else "正确。"
        for i in range(len(options))
    ]
    return {
        "id": qid,
        "chapterId": "ch6",
        "sectionId": section_id,
        "sectionTitle": section_title,
        "sourceId": source,
        "sourceType": "rainclassroom",
        "type": "single",
        "stem": stem,
        "options": options,
        "answerIndex": answer_index,
        "explanation": explanation,
        "relatedTopicId": related,
        "tags": ["老师测试", "雨课堂", tag],
        "optionExplanations": notes,
        "optionTopicIds": [related] * len(options),
    }


